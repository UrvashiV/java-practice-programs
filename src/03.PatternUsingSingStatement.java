class PatternUsingSingStatement {
     static void main() {
        System.out.println("This is our first pattern");
        System.out.println("* \n* * \n* * * \n* * * *\n* * * * * ");
        System.out.println("This is our second pattern");
        System.out.println("* * * * *\n* * * *\n* * *\n* *\n* ");
        System.out.println("This is our third pattern");
        System.out.println("        * \n      * * \n    * * * \n  * * * * \n* * * * * ");
    }
}
